class Math {
    public int max(int a, int b){
    if(a > b){
        return a;
    }
    else if (b > a){
        return b;
    }
    else{
        return a;
    }
}
public int min(int a, int b){
    if(a > b){
        return b;
    }
    else if (b > a){
        return a;
    }
    else{
        return a;
    }
}
}